# Suffixes
# A question mark is used in methods like empty? to return a boolean
# if some condition is met.
empty?

# An exclamation mark at the end of a method name is used to indicate
# that caution is required with the use of the method. Usually it's a mutator
# method taht alters the object in place.
sort!

# Ruby variables
# Prefixes tell the scope of the variable.
$global
@instance_variable
@@class_variable
local_variable
